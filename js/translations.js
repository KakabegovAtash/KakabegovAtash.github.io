// Function to translate the page
window.applyTranslations = function(lang) {
  const dict = window.PORTFOLIO_TRANSLATIONS[lang];
  if (!dict) return;

  // Set html tag lang attribute
  document.documentElement.setAttribute('lang', lang);

  // Find all elements with data-i18n attribute
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key]) {
      // Special handle for copy buttons
      if (el.classList.contains('btn-copy')) {
        // If it's currently showing 'Copied' state, just update its dataset value
        el.setAttribute('data-translated-copy', dict['btn-copy']);
        el.setAttribute('data-translated-copied', dict['btn-copied']);
        if (!el.classList.contains('copied')) {
          el.textContent = dict[key];
        }
      } else if (el.hasAttribute('data-i18n-html')) {
        el.innerHTML = dict[key];
      } else {
        el.textContent = dict[key];
      }
    }
  });

  // Update meta elements
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && dict['meta-description']) {
    metaDesc.setAttribute('content', dict['meta-description']);
  }

  // Update page title
  if (dict['page-title']) {
    document.title = dict['page-title'];
  }

  // Update current language label on selector
  const langLabel = document.querySelector('.lang-code-label');
  if (langLabel) {
    langLabel.textContent = lang;
  }

  // Highlight active dropdown item
  const dropdownItems = document.querySelectorAll('.lang-dropdown-item');
  dropdownItems.forEach(item => {
    if (item.getAttribute('data-lang') === lang) {
      item.classList.add('active');
    } else {
      item.classList.remove('active');
    }
  });

  // Update side nav items text
  if (window.updateSideNav) {
    window.updateSideNav();
  }
};
